class TimeLog {
  String description = "";
  int hour = 0;

  TimeLog() {
    description;
    hour;
  }

  getHour() {
    return hour;
  }
  getDescription() {
    return description;
  }

  setHour(int hour) {
    this.hour = hour;
  }
  setDescription(String description) {
    this.description = description;
  }
}